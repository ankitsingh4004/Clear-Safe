package com.workorder.app.workorderapplication.model.history;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

/**
 * Created by Bharat Tripathi on 30-May-18.
 */

public class HistoryDetailsModel {
    @SerializedName("propertyName")
    @Expose
    private String propertyName;
    @SerializedName("previousValue")
    @Expose
    private String previousValue;
    @SerializedName("savedValue")
    @Expose
    private String savedValue;
    @SerializedName("isChanged")
    @Expose
    private Boolean isChanged;

    public String getPropertyName() {
        return propertyName;
    }

    public void setPropertyName(String propertyName) {
        this.propertyName = propertyName;
    }

    public String getPreviousValue() {
        return previousValue;
    }

    public void setPreviousValue(String previousValue) {
        this.previousValue = previousValue;
    }

    public String getSavedValue() {
        return savedValue;
    }

    public void setSavedValue(String savedValue) {
        this.savedValue = savedValue;
    }

    public Boolean getIsChanged() {
        return isChanged;
    }

    public void setIsChanged(Boolean isChanged) {
        this.isChanged = isChanged;
    }
}
